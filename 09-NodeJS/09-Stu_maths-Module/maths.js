var sum = function(a,b){
    return a+b;
}
var difference = function(a,b){
    return a-b;
}
var product = function(a,b){
    return a*b;
}
var quotient = function(a,b){
    return a/b;
}

module.exports = {
    sum: sum,
    difference: difference, 
    product: product,
    quotient: quotient
  };